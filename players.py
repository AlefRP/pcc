# Slicing, verificando fatias de lista
players = ['charles', 'martina', 'michael', 'florence', 'eli']
print(players[0:3])

# Imprime do segundo ao quarto elemento
print(players[1:4])

# Se omitir o primeiro elemento, o slice começa do primeiro elemento
print(players[:4])

# Se omitir o último elemento, o slice termina no último elemento
print(players[2:])

# Pegar os 3 ultimos
print(players[-3:])